const arr = [1, 2, 3, 3, 5];

// filter array
console.log(arr.filter(item => item === 3));
// return the first element of the array without function
console.log(arr[0])

// return the first element of the array with function
console.log(arr.shift());

// return the last element of the array without function
console.log(arr[arr.length -1])

// return the last element of the array with function
console.log(arr.pop());

